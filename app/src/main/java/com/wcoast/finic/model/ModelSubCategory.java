package com.wcoast.finic.model;

public class ModelSubCategory {
    private String subcatName;
    private int subcatId;
    private String subcatImage;
    private int subcatCatid;
    private String subcatDetail;
    private String subcatLink;

    public ModelSubCategory() {

    }

    public String getSubcatName() {
        return subcatName;
    }

    public void setSubcatName(String subcatName) {
        this.subcatName = subcatName;
    }

    public int getSubcatId() {
        return subcatId;
    }

    public void setSubcatId(int subcatId) {
        this.subcatId = subcatId;
    }

    public String getSubcatImage() {
        return subcatImage;
    }

    public void setSubcatImage(String subcatImage) {
        this.subcatImage = subcatImage;
    }

    public int getSubcatCatid() {
        return subcatCatid;
    }

    public void setSubcatCatid(int subcatCatid) {
        this.subcatCatid = subcatCatid;
    }

    public String getSubcatDetail() {
        return subcatDetail;
    }

    public void setSubcatDetail(String subcatDetail) {
        this.subcatDetail = subcatDetail;
    }

    public String getSubcatLink() {
        return subcatLink;
    }

    public void setSubcatLink(String subcatLink) {
        this.subcatLink = subcatLink;
    }
}
